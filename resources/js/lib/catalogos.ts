// true si NO es boliviano (acepta gentilicio "BOLIVIANO" o país "BOLIVIA"/"BOLIVIANA").
export const esExtranjero = (nacionalidad?: string): boolean => {
    const n = (nacionalidad ?? '').trim().toUpperCase();
    return n !== '' && !n.startsWith('BOLIV');
};

// Devuelve el país de una nacionalidad (gentilicio). Si no la encuentra, devuelve el mismo texto.
export const paisDe = (nacionalidad?: string): string => {
    const n = (nacionalidad ?? '').trim().toUpperCase();
    return PAIS_POR_NACIONALIDAD[n] ?? n;
};